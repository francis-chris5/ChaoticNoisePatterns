
package com.csfrancis555.chaoticnoisepatterns;


public class ChaoticSVG {
    private String name;
    private String svg;

    public ChaoticSVG() {
    }

    public ChaoticSVG(String name, String svg) {
        this.name = name;
        this.svg = svg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSvg() {
        return svg;
    }

    public void setSvg(String svg) {
        this.svg = svg;
    }
    
    
}
